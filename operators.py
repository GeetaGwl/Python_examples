'''1) Arithmetical operators
    +,-,*,/,//,**,%'''
a,b=10,3
print("sum",a+b)
print("sub",a-b)
print("prod",a*b)
print("div",a/b)
print("div in int",a//b)
print("mod is",a%b)

'''2) Relational Operators
   >,<,>=,<=,==,!='''
print(a>b)
print(a<b)
print(a==b)
print(a!=b)

'''3) Logical operators
   (and,or)'''
print(a==10 and a<b)   
'''
4)Bitwise Operators
  &,|,^,~,>>,<<'''

print(a&b,a|b,a^b,~a)  

'''5)Membership Operators  
  in,not in'''
x="Geeta"
print('e' in x)  

'''
6)Identity Operators
  is, is not'''

print(x is "Geeta")






